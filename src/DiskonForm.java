    private void hitungDiskon() {
    try {
        double hargaAsli = Double.parseDouble(txtHargaAsli.getText());
        int diskon = sldDiskon.getValue();
        
        double hargaSetelahDiskon = hargaAsli - (hargaAsli * diskon / 100);
        double penghematan = hargaAsli - hargaSetelahDiskon;
        
        // Cek kode kupon
        String kupon = txtKupon.getText().trim();
        if (kupon.equalsIgnoreCase("HEMAT10")) {
            hargaSetelahDiskon *= 0.9; // tambahan 10%
        } else if (kupon.equalsIgnoreCase("DISKON5")) {
            hargaSetelahDiskon *= 0.95;
        }
        
        DecimalFormat df = new DecimalFormat("#,###.00");

        // âœ… tampilkan hasil di textfield (bukan label agar tidak terpotong)
        txtHargaAkhir.setText("Rp " + df.format(hargaSetelahDiskon));
        txtHemat.setText("Rp " + df.format(penghematan));

        // Tambahkan ke riwayat
        txtRiwayat.append("Harga Asli: Rp " + df.format(hargaAsli) +
                          ", Diskon: " + diskon + "%, Kupon: " + kupon +
                          ", Harga Akhir: Rp " + df.format(hargaSetelahDiskon) + "\n");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Masukkan harga asli dengan benar!", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    

